##USD to CAD converter
#This conversion rate is based on 5/8/2024

print("USD to CAD converter")
print("\n")

usd = 50
cad = 111

usd_to_cad = usd * 1.37
cad_to_usd = cad / 1.37

print(usd, "usd is", usd_to_cad, "cad")
print(cad, "cad is", cad_to_usd, "usd")
print("\n\n\n")


